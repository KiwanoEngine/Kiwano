#pragma once
#include "base.h"

namespace easy2d
{
	namespace window
	{
		typedef struct Property
		{
			String	title;	/* 标题 */
			int		width;	/* 宽度 */
			int		height;	/* 高度 */
			LPCWSTR	icon;	/* 图标 */

			Property()
				: title(L"Easy2D Game")
				, width(640)
				, height(480)
				, icon(nullptr)
			{}
		} Property;

		class WindowInfo
		{
		public:
			HWND handle;
			float xscale;
			float yscale;

		public:
			WindowInfo();

			void Initialize(
				const Property& property
			);

			void Destroy();

			// 获取标题
			String GetTitle() const;

			// 设置标题
			void SetTitle(const String& title);

			// 获取窗口大小
			Size GetSize() const;

			// 获取窗口宽度
			float GetWidth() const;

			// 获取窗口高度
			float GetHeight() const;

			// 重设窗口大小
			void SetSize(int width, int height);

			// 设置窗口图标
			void SetIcon(LPCWSTR icon_resource);
		};

		extern WindowInfo instance;
	}
}
